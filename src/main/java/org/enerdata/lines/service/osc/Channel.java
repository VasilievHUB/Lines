package org.enerdata.lines.service.osc;


public class Channel {
	
	private int id;
	private int number;
	private String name;
	 
	public int getId() {
		return id;
	}
	public int getNumber() {
		return number;
	}
	public void setNumber(int number) {
		this.number = number;
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}

	
}
